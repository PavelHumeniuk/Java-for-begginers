public class Main {
   public static void main(String[] args) {
      int a = 5;
      int b = 6;

      // результат вычислений можно записать в новую переменную
      int c = a + b;

      System.out.println(c);

      // а можно сразу напечатать
      System.out.println(a * c);
      System.out.println(c / a);

      // попробуй ниже перемножить все 3 переменные (a, b, c) и напечатать в консоль

   }
}